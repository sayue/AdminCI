        中山大学移民与族群研究中心以国内外移民群体、族群与族群关系为主要研究领域，科研课题涉及人类学、民族学、社会学、人文地理、文化遗产（包括物质文化遗产和非物质文化遗产）、社会风险评估、移民与环境评估等，目前形成的三个主要研究方向为：国际移民研究、国内移民研究、移民与族群关系研究。国内移民的研究主要关注自愿迁移至国内其它城市居住和工作的城市移民，以及征地拆迁引发的非自愿的政策性移民两大群体。国际移民研究的研究将结合国际移民与迁徙的前沿理论，探讨不同形式的移民与族群关系。移民与族群关系研究的范围包括，人口迁移与民族贫困地区的发展研究、城市中的族群与族群关系研究、移民族群及本土化的研究等。
