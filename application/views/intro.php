                <div id="collapseFour" class="panel-collapse collapse">
                    <div class="panel-body">
        1、国内移民的研究主要关注自愿迁移至国内其它城市居住和工作的城市移民，以及征地拆迁引发的非自愿的政策性移民两大群体。本方向的研究的特点在于，首先，有着深厚的学术传统和积累；第二，有得到国内外学术界认可的关于中国农民工迁移的理论总结和概况，如周大鸣教授提出的“钟摆理论”、“二元社区”等；第三，在研究方法和手段上具有跨学科的特点。本方向制定的任务是，总结我国移民发展的规律、机制和变化趋势；比较不同类型移民在迁移和适应过程中的异同；把移民研究与我国社会经济发展的整体相联系，探索人口与发展的互动机制。
2、国际移民研究的研究将结合国际移民与迁徙的前沿理论，探讨不同形式的移民与族群关系。在全球化与华人移民研究方面，突出以跨文化比较为基础的田野调查与人类学研究，探讨了全球化时空下的中国与世界以及各地华人文化的多样性及其持续与变迁。本方向的研究还包括，国际移民的适应研究、国际移民社会空间分异、融合与管控机制的研究等。本方向的特点包括，在继承社区调查的学术传统的同时，将引入跨国网络与民族学文化圈等理论路径，进一步关注全球化语境中移民群体的族群文化与社会网络；注重海外民族志研究，在扎实的实地田野调查基础上，展开移民类型、现象与动态的跨文化比较研究。推动国内学界广泛参与国际学界跨国移民研究的讨论，丰富相关研究经验，加强学术交流与对话，进而拓展国内学界有关国际移民研究的理论和方法，积累国际移民，特别是以往极少关注的移入的国际移民群体的经验积累。
3、移民与族群关系研究的范围包括，人口迁移与民族贫困地区的发展研究、城市中的族群与族群关系研究、移民族群及本土化的研究等。本方向的特点为，首先，具有深厚的得到国内外学术界认可的学术积累和传统，中山大学自上个世纪20年代起就确立了以南方族群和文化研究为重点，辐射全国和东南亚的族群与族群关系研究的宗旨；第二，拥有实力雄厚的研究团队。中心的校内外、专兼职研究人员中包括藏族、回族等数十个少数民族研究员，他们在新疆、西藏等民族地区扎根研究，并产出系列具有国内外影响力的研究成果。本方向拟定的任务是，在研究理论和方法上尝试突破性实践，将移民与群体结合起来研究，打破过去研究单一族群或单一区域文化的局限性。同时，在实践层面，通过把握民族问题发展的动向，为化解民族问题，处理民族矛盾和冲突提供建议。

